import UIKit

extension UIColor {

    static func fieldBorderColor() -> UIColor
    {
        return UIColor(red: 188/255, green: 189/255, blue: 195/255, alpha: 1.0)
    }
    
    static func themeColor() -> UIColor
    {
        return UIColor(red: 40.0/255.0, green: 204.0/255.0, blue: 252.0/255.0, alpha: 1.0)
    }
}
